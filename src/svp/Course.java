
package svp;

public class Course {
    private String name;
    private String Rating;

    public void setName(String name) {
        this.name = name;
    }

    public void setRating(String Rating) {
        this.Rating = Rating;
    }

    public String getName() {
        return name;
    }

    public String getRating() {
        return Rating;
    }
    
}
